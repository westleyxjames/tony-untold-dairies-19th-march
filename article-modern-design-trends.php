                    <h1 class="article-main-title">Design Trends That Are Defining Modern Living Spaces</h1>
                    <p class="article-subtitle">Explore the interior design trends shaping contemporary homes in 2026</p>
                    <div class="article-author-info">
                        <span class="author-name">By Jennifer Walsh</span>
                        <span style="color: #6b6b6b;">•</span>
                        <span style="color: #6b6b6b;">February 20, 2026</span>
                    </div>
                </div>

                <div class="article-featured-image">
                    <img src="https://images.unsplash.com/photo-1705321963943-de94bb3f0dd3?crop=entropy&cs=tinysrgb&fit=max&fm=jpg&q=80&w=1080" alt="Modern Interior Design">
